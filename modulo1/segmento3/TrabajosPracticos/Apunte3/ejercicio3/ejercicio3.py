num = input("Ingrese un numero romano para la conversion a decimal: ")

if (num == 'i'):
	print("El numero romano ingresado es I y su conversion a decimal es 1")
elif (num == 'v'):
	print("El numero romano ingresado es V y su conversion a decimal es 5")
elif (num == 'x'):
	print("El numero romano ingresado es X y su conversion a decimal es 10")
elif (num == 'l'):
	print("El numero romano ingresado es L y su conversion a decimal es 50")
elif (num == 'c'):
	print("El numero romano ingresado es C y su conversion a decimal es 100")
elif (num == 'd'):
	print("El numero romano ingresado es D y su conversion a decimal es 500")
elif (num == 'm'):
	print("El numero romano ingresado es M y su conversion a decimal es 1000")
else:
	print("Error. Ud. no ingreso un numero romano.")
